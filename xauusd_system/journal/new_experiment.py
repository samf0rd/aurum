"""
journal/new_experiment.py — auto-generate a journal entry template.

Usage:
  python journal/new_experiment.py 003 "Entry confirmation delay bar+1"

Creates journal/EXP-003.md with a skeleton pre-filled for the experiment.
"""
import sys
from datetime import date
from pathlib import Path

_JOURNAL_DIR = Path(__file__).resolve().parent


def main() -> None:
    if len(sys.argv) < 3:
        print("Usage: python journal/new_experiment.py <NNN> \"<title>\"", file=sys.stderr)
        print('  e.g. python journal/new_experiment.py 003 "Entry confirmation delay bar+1"',
              file=sys.stderr)
        sys.exit(1)

    num   = sys.argv[1].zfill(3)
    title = sys.argv[2]
    exp_id = f"EXP-{num}"
    today  = date.today().isoformat()
    out    = _JOURNAL_DIR / f"{exp_id}.md"

    if out.exists():
        print(f"ERROR: {out} already exists — aborting to avoid overwrite.", file=sys.stderr)
        sys.exit(1)

    content = f"""\
# {exp_id}: {title}

**Date:** {today}
**Status:** IN PROGRESS
**Results file:** results/exp-{num}.json
**Experiment config:** experiments/exp-{num}.yaml

## Hypothesis
[fill in]

## Parameters
[fill in — copy from experiments/exp-{num}.yaml when created]

## Results summary
[fill in after running: python run_experiment.py exp-{num}]

## Diagnostic findings
[fill in after running: python backtest_runner.py --analyse results/exp-{num}.json]

## Verdict
[ACCEPTED / REJECTED / INCONCLUSIVE]

## What changed vs previous experiment
[fill in]

## Next experiment
[fill in]

---
*Auto-generated by journal/new_experiment.py*
"""

    out.write_text(content, encoding="utf-8")
    print(f"Created journal/{exp_id}.md — fill in hypothesis, run experiment, paste results.")


if __name__ == "__main__":
    main()
